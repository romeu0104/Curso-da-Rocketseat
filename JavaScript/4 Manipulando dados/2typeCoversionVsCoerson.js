/*  Type conversion (typecasting) vs Type coersion

    Trata-se da conversão de dados de um tipo para outro
    *Conversion é quando nó fazemos essa alteração manualmente
    *Coersion é feita altomáticamente pelo JS
    */

// Exmplo de Coersion
console.log("9" + 5)// aqui JS vai converter o número 5 em uma string e concatenar os dados imprimindo 95
console.log(Number("9") + 5) //aqui usando uma função converto a string 9 em número e será impresso a soma = 14