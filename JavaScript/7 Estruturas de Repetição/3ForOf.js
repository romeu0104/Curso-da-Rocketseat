// For of

//Quando quero criar uma repetição com as condicionais de alguma coisa

//Exemplo1: repetir até treminar as letras de uma string

let name="Romeu"

for(let letra of name)(//criou uma variável "letra" para atribuir cada elemento da string e repetrir até acabar
    console.log(letra)
)

//Exemplo 2: repetir até acabar as posições de um array

names=["Romeu", "Dayane", "Helena"]
for(let name of names){ //Essa variável name, diferente da anterior só vai existir dentro desse escopo
    console.log(name)
}